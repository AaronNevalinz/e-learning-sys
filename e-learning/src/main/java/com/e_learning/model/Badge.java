package com.e_learning.model;

import jakarta.persistence.*;

@Entity
public class Badge {
    @Id @GeneratedValue
    private Long id;

    @Column(unique = true)
    private String code;

    private String name;
    private String icon;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }
}
